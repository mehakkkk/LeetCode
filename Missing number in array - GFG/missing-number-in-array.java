class Solution {
    int missingNumber(int array[], int n) {
        
        //sum of n natural number
        int res1 = 0,res2 = n;
        for(int i =0;i<n-1;i++)
        {
            res1 = res1 ^ array[i];
            
            res2 = res2 ^ (i+1);
        }
        
        return res1^res2;
    }
}